template <typename T> void Release(T * &p)
{
	if(p != null)
	{
		p->Release();
		p = null;
	}
}

//////////////////////////////////////////////////////////////////////

template <typename T> void Delete(T * &p)
{
	if(p != null)
	{
		delete p;
		p = null;
	}
}

//////////////////////////////////////////////////////////////////////

template <typename T> void DeleteArray(T * &p)
{
	if(p != null)
	{
		delete[] p;
		p = null;
	}
}

